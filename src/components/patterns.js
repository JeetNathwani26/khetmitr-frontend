// Gmail pattern
export const gmailPattern = /^[a-zA-Z0-9._%+-]+@gmail\.com$/;

// Strong password pattern
export const passwordPattern = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/;

// PDF file pattern
export const pdfPattern = /\.pdf$/i;

// Phone number pattern (10 digits only)
export const phonePattern = /^\d{10}$/;



